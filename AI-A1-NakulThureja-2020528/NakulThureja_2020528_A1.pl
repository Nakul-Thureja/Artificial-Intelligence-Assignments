union([], List, List).
union([Head|Tail1], List2, List3) :- member(Head, List2), ! , union(Tail1, List2, List3).
union([Head|Tail1], List2, [Head|Tail3]) :- union(Tail1, List2, Tail3).

intersect([], _, []).
intersect([Head|Tail1], List2, List3) :- member(Head, List2), !, List3 = [Head|Tail3], intersect(Tail1, List2, Tail3).
intersect([_|Tail1], List2, List3) :- intersect(Tail1, List2, List3).

list_length([]     , 0 ).
list_length([_|Tail] , Len ) :- list_length(Tail,L) , Len is L+1 .

courseList(0,_,_,_):-
    write("System highly recommends you to repeat your core courses").

courseList(_,S,X,Y):-
    setof(Z1,(coursesem(Z1,X)),R1),
    setof(Z2,(corecourses(S,Z2)),R2),
    intersect(R1,R2,Rf),list_length(Rf,Lf),
    write("Your course courses this semester are: "),nl,print_list(Rf,1),
    (Lf=:=5 -> write("You cannot Take any Elective this Semester. All The Best for your sem :)");electiveList(S,X,Y)).

electiveList(_,"2",_):-
    nl,write("You can only choose SSH courses this Semester: "),nl,
    write("SSH courses this Semester offered are: "),nl,
    setof(Z1,(coursesem(Z1,"2"),courseinterest(Z1,"SSH")),Results1),print_list(Results1,1),nl,nl,
    write("You can only take these electives this Semester. All The Best for your sem :)").

electiveList(S,X,Y):- 
    nl,write("Have you completed your SSH courses?? "),nl,
    write("SSH courses this Semester offered are: ") ,nl,
    setof(Z2,(coursesem(Z2,X),courseinterest(Z2,"SSH")),Results2),print_list(Results2,1),nl,nl,
    write("Are you satisfied?"),nl,
    write("Do you want the system to recommend courses based on your Interest (Y=1/N=0)"),read(InterestedCourses),
    InterestedCourses > 0 -> printInterestedCourses(S,X,Y) ; write("Hope you Enjoyed. All The Best for your sem :)").

printInterestedCourses(S,X,Y):- 
    setof(Z1,(coursesem(Z1,X)),Result1),
    setof(Z2,(courseinterest(Z2,Y)),Result2),    
    setof(Z3,(courselevel(Z3,"0")),Result3),
    setof(Z4,(courselevel(Z4,"1")),Result4),
    intersect(Result1,Result2,Results),
    intersect(Results,Result3,ResultBasic),   
    intersect(Results,Result4,ResultAdv),
    nl,write("Based on your interests these are the best suited basic courses for you: "),nl,   
    print_list(ResultBasic,1),nl,
    write("The System highly recommends you to pursue these basic courses before moving up to advanced courses in the same field"),nl,
    write("Have you done these (Y=1,N=0): "), read(DoneCourses),
    (DoneCourses > 0 -> (nl,write("Based on your interests these are the best suited advanced courses for you: "),nl,print_list(ResultAdv,1),nl,nl);write("Great!!"),nl),
    recommendAgain(S,X,Y).

    
recommendAgain(S,X,_):- 
    retractall(career(_)),
    write("Are you satisfied with your options?"),nl, 
    write("Do you want to change your interest (Y=1,N=0): "), read(ChangeInterest),
    (ChangeInterest>0 -> (nl,write("What is the field you want to pursue: (Enter the corresponding no.)"),nl,
    print_list(["Machine Learning","Cyber Security","Computer Architechture","UI-UX Designer","Computational Biology","ELectronics","Mathematics and Computing"],1),nl,
    read(D),interest(D),(career(K)),printInterestedCourses(S,X,K));write("Hope you Enjoyed. All The Best for your sem :)")).

    
